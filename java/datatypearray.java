public class datatypearray{
	public static void main(String[]args){
		int integer[] = new int[]{34,45,56,66};
		char names[] = new char[]{'f','e','g'};
		double decimal[] = new double[]{2.2,4.4,5.6};
		boolean condition[] = new boolean[]{true,false};

		
		
		
		System.out.println(integer[0]);
		System.out.println(integer[1]);
		System.out.println(integer[3]);
		System.out.println(names[0]);
		System.out.println(names[2]);
		System.out.println(decimal[0]);
		System.out.println(decimal[2]);
		System.out.println(condition[1]);
		System.out.println(condition[0]);
	}
}
